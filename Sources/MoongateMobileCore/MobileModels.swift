/// A portable task snapshot that can be persisted or mirrored across mobile UI and workers.
public struct MobileTaskSnapshot: Codable, Sendable, Equatable, Identifiable {
    public var id: String
    public var platform: MobilePlatform
    public var state: MobileTaskState
    public var progress: MobileTaskProgress
    public var downloadSelection: MobileDownloadSelection?
    public var exportProfile: MobileExportProfile
    public var capabilities: MobileProcessingCapabilities
    public var backgroundPolicy: MobileBackgroundPolicy
    public var executionGenerationID: String?
    public var result: MobileTaskResult?
    public var error: MobileTaskError?

    public var availableActions: [MobileTaskAction] {
        switch state {
        case .waiting, .ready:
            return [.startDownload, .cancel]
        case .analyzing:
            return [.cancel]
        case .downloading, .translating, .exporting:
            return backgroundPolicy.canResume ? [.pause, .cancel] : [.cancel]
        case .needsForegroundToContinue:
            return [.openAppToContinue, .cancel]
        case .completed:
            guard let result, !result.artifacts.isEmpty else {
                return [.remove]
            }
            var actions: [MobileTaskAction] = []
            if (exportProfile.subtitleMode == .translatedSubtitleFile ||
                exportProfile.subtitleMode == .softSubtitle),
               result.artifacts.contains(where: { $0.kind == .transcript }),
               !result.artifacts.contains(where: { artifact in
                   switch exportProfile.subtitleMode {
                   case .translatedSubtitleFile:
                       return artifact.kind == .translatedSubtitleFile
                   case .softSubtitle:
                       return artifact.kind == .softSubtitle
                   case .none, .burnedInSubtitle:
                       return false
                   }
               }) {
                actions.append(.exportTranslatedSubtitle)
            }
            if exportProfile.requiresVideoRender,
               capabilities.canSatisfy(exportProfile),
               result.artifacts.contains(where: { $0.kind == .originalMedia }),
               result.artifacts.contains(where: { $0.kind == .translatedSubtitleFile }),
               !result.artifacts.contains(where: { $0.kind == .renderedVideo }) {
                actions.append(.exportRenderedVideo)
            }
            actions.append(contentsOf: [.openResult, .shareResult, .remove])
            return actions
        case .failed:
            return [.retry, .remove]
        case .cancelled:
            return [.remove]
        }
    }

    public init(
        id: String,
        platform: MobilePlatform,
        state: MobileTaskState = .waiting,
        progress: MobileTaskProgress = MobileTaskProgress(),
        downloadSelection: MobileDownloadSelection? = nil,
        exportProfile: MobileExportProfile = MobileExportProfile(),
        capabilities: MobileProcessingCapabilities? = nil,
        backgroundPolicy: MobileBackgroundPolicy = MobileBackgroundPolicy(),
        executionGenerationID: String? = nil,
        result: MobileTaskResult? = nil,
        error: MobileTaskError? = nil
    ) {
        self.id = id
        self.platform = platform
        self.state = state
        self.progress = progress
        self.downloadSelection = downloadSelection
        self.exportProfile = exportProfile
        self.capabilities = capabilities ?? MobileProcessingCapabilities(platform: platform)
        self.backgroundPolicy = backgroundPolicy
        self.executionGenerationID = executionGenerationID
        self.result = result
        self.error = error
    }
}

