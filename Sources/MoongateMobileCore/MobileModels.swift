public enum MobileTranslationCredentialRequirement: String, Codable, Sendable, Equatable, CaseIterable {
    case none
    case secureCredential
    case localModel
    case runtimeEntitlement
}

public struct MobileTranslationConfiguration: Codable, Sendable, Equatable {
    public var engine: TranslationEngine
    public var baseURL: String?
    public var model: String?
    public var credential: SecureCredentialReference?
    public var readiness: TranslationReadiness

    public var credentialRequirement: MobileTranslationCredentialRequirement {
        switch engine {
        case .anthropicCompatible, .openAICompatible:
            return .secureCredential
        case .appleTranslationLowLatency, .appleTranslationHighFidelity:
            return .runtimeEntitlement
        case .appleFoundationOnDevice:
            return .localModel
        case .appleFoundationPCC, .appleFoundationCloudPro:
            return .runtimeEntitlement
        }
    }

    public var usesCloudService: Bool {
        switch engine {
        case .anthropicCompatible, .openAICompatible, .appleFoundationPCC, .appleFoundationCloudPro:
            return true
        case .appleTranslationLowLatency, .appleTranslationHighFidelity, .appleFoundationOnDevice:
            return false
        }
    }

    public var isRunnableWithoutUserCredential: Bool {
        credentialRequirement != .secureCredential
    }

    public static func conservativeReadiness(for engine: TranslationEngine) -> TranslationReadiness {
        switch engine {
        case .anthropicCompatible, .openAICompatible:
            return TranslationReadiness(issues: [
                TranslationReadinessIssue(kind: .needsConfiguration)
            ])
        case .appleTranslationLowLatency, .appleTranslationHighFidelity:
            return TranslationReadiness(issues: [
                TranslationReadinessIssue(kind: .needsRuntimeVerification),
                TranslationReadinessIssue(kind: .needsLanguageDownload)
            ])
        case .appleFoundationOnDevice:
            return TranslationReadiness(issues: [
                TranslationReadinessIssue(kind: .appleIntelligenceUnavailable),
                TranslationReadinessIssue(kind: .modelUnavailable)
            ])
        case .appleFoundationPCC:
            return TranslationReadiness(issues: [
                TranslationReadinessIssue(kind: .pccUnavailable)
            ])
        case .appleFoundationCloudPro:
            return TranslationReadiness(issues: [
                TranslationReadinessIssue(
                    kind: .pccUnavailable,
                    message: "Apple Intelligence Cloud Pro（云端 Pro）当前不可用。"
                )
            ])
        }
    }

    public init(
        engine: TranslationEngine,
        baseURL: String? = nil,
        model: String? = nil,
        credential: SecureCredentialReference? = nil,
        readiness: TranslationReadiness? = nil
    ) {
        self.engine = engine
        self.baseURL = baseURL
        self.model = model
        self.credential = credential
        self.readiness = readiness ?? Self.conservativeReadiness(for: engine)
    }
}

