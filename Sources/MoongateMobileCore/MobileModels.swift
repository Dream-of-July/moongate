/// Coarse capabilities available to a mobile worker on a given platform.
public enum MobileProcessingCapability: String, Codable, Sendable, Equatable, CaseIterable {
    case analysis
    case download
    case translation
    case subtitleExport
    case videoRender
    case backgroundTransfer
    case backgroundRender
    case localTranslationModel
    case cloudTranslation
    case appleIntelligence
}

public struct MobileProcessingCapabilities: Codable, Sendable, Equatable {
    public var platform: MobilePlatform
    public var supportedCapabilities: [MobileProcessingCapability]
    public var maxRenderHeight: Int?

    public func supports(_ capability: MobileProcessingCapability) -> Bool {
        supportedCapabilities.contains(capability)
    }

    public func canSatisfy(_ profile: MobileExportProfile) -> Bool {
        switch profile.subtitleMode {
        case .none:
            return supports(.download)
        case .translatedSubtitleFile:
            return supports(.translation) && supports(.subtitleExport)
        case .softSubtitle:
            return supports(.subtitleExport)
        case .burnedInSubtitle:
            guard supports(.videoRender) else { return false }
            guard let requestedHeight = profile.maxRenderHeight,
                  let maxRenderHeight else {
                return true
            }
            return requestedHeight <= maxRenderHeight
        }
    }

    public init(
        platform: MobilePlatform,
        supportedCapabilities: [MobileProcessingCapability] = [],
        maxRenderHeight: Int? = nil
    ) {
        self.platform = platform
        self.supportedCapabilities = supportedCapabilities
        self.maxRenderHeight = maxRenderHeight
    }
}

