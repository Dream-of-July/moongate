public struct APICompatibleMobileTranslationProvider: MobileTranslationProvider {
    public var configuration: MobileTranslationConfiguration

    private let credentialStore: any SecureCredentialStore
    private let transport: any MobileTranslationTransport

    public init(
        configuration: MobileTranslationConfiguration,
        credentialStore: any SecureCredentialStore,
        transport: any MobileTranslationTransport
    ) {
        self.configuration = configuration
        self.credentialStore = credentialStore
        self.transport = transport
    }

    public func readiness(for context: TranslationContext) async -> TranslationReadiness {
        do {
            _ = try await validatedConfiguration()
            _ = try endpointURL(
                baseURL: configuration.baseURL ?? "",
                endpointPath: configuration.engine == .openAICompatible ? "/v1/chat/completions" : "/v1/messages"
            )
            return .ready
        } catch {
            return TranslationReadiness(issues: [TranslationReadinessIssue(kind: .needsConfiguration)])
        }
    }

    public func translate(_ request: MobileTranslationRequest) async throws -> MobileTranslationResult {
        let valid = try await validatedConfiguration()
        let transportRequest: MobileTranslationTransportRequest
        switch configuration.engine {
        case .openAICompatible:
            transportRequest = try makeOpenAIRequest(valid: valid, translationRequest: request)
        case .anthropicCompatible:
            transportRequest = try makeAnthropicRequest(valid: valid, translationRequest: request)
        case .appleTranslationLowLatency,
             .appleTranslationHighFidelity,
             .appleFoundationOnDevice,
             .appleFoundationPCC,
             .appleFoundationCloudPro:
            throw MobileTranslationProviderError.unsupportedEngine
        }

        let response = try await transport.send(transportRequest)
        guard response.statusCode == 200 else {
            throw MobileTranslationProviderError.requestFailed(response.statusCode)
        }
        return try parseTranslationResponse(response.body, original: request)
    }

    private struct ValidConfiguration {
        var baseURL: String
        var model: String
        var secret: String
    }

    private func validatedConfiguration() async throws -> ValidConfiguration {
        guard configuration.engine == .openAICompatible || configuration.engine == .anthropicCompatible else {
            throw MobileTranslationProviderError.unsupportedEngine
        }
        guard let baseURL = configuration.baseURL?.trimmingCharacters(in: .whitespacesAndNewlines),
              !baseURL.isEmpty,
              let model = configuration.model?.trimmingCharacters(in: .whitespacesAndNewlines),
              !model.isEmpty,
              let credential = configuration.credential else {
            throw MobileTranslationProviderError.invalidConfiguration
        }
        guard let secret = try await credentialStore.credential(for: credential)?
            .trimmingCharacters(in: .whitespacesAndNewlines),
            !secret.isEmpty else {
            throw MobileTranslationProviderError.missingCredential
        }
        return ValidConfiguration(baseURL: baseURL, model: model, secret: normalizedBearerSecret(secret))
    }

    private func makeOpenAIRequest(
        valid: ValidConfiguration,
        translationRequest: MobileTranslationRequest
    ) throws -> MobileTranslationTransportRequest {
        // 用最通用的 Chat Completions 端点：官方/Azure 及绝大多数"OpenAI 兼容"网关都支持，
        // 而 /v1/responses 是 OpenAI 私有新端点，多数兼容服务不实现，会导致请求失败。
        let url = try endpointURL(baseURL: valid.baseURL, endpointPath: "/v1/chat/completions")
        struct Message: Encodable {
            let role: String
            let content: String
        }
        struct Payload: Encodable {
            let model: String
            let messages: [Message]
            let max_completion_tokens: Int
        }
        let instructions = "Translate each numbered subtitle segment to \(translationRequest.context.targetLanguage). Preserve numbering as '<id>=<translation>'."
        let payload = Payload(
            model: valid.model,
            messages: [
                Message(role: "system", content: instructions),
                Message(role: "user", content: numberedInput(from: translationRequest.segments))
            ],
            max_completion_tokens: max(1024, translationRequest.segments.count * 128)
        )
        return MobileTranslationTransportRequest(
            url: url,
            headers: [
                "content-type": "application/json",
                "Authorization": "Bearer \(valid.secret)"
            ],
            body: try JSONEncoder().encode(payload)
        )
    }

    private func makeAnthropicRequest(
        valid: ValidConfiguration,
        translationRequest: MobileTranslationRequest
    ) throws -> MobileTranslationTransportRequest {
        let url = try endpointURL(baseURL: valid.baseURL, endpointPath: "/v1/messages")
        struct Message: Encodable {
            let role: String
            let content: String
        }
        struct Payload: Encodable {
            let model: String
            let max_tokens: Int
            let system: String
            let messages: [Message]
        }
        let payload = Payload(
            model: valid.model,
            max_tokens: max(1024, translationRequest.segments.count * 128),
            system: "Translate each numbered subtitle segment to \(translationRequest.context.targetLanguage). Preserve numbering as '<id>=<translation>'.",
            messages: [Message(role: "user", content: numberedInput(from: translationRequest.segments))]
        )
        return MobileTranslationTransportRequest(
            url: url,
            headers: anthropicHeaders(url: url, secret: valid.secret),
            body: try JSONEncoder().encode(payload)
        )
    }

    private func anthropicHeaders(url: URL, secret: String) -> [String: String] {
        var headers = [
            "content-type": "application/json",
            "anthropic-version": "2023-06-01",
            "x-api-key": secret
        ]
        if url.host?.lowercased() != "api.anthropic.com" {
            headers["Authorization"] = "Bearer \(secret)"
        }
        return headers
    }

    private func parseTranslationResponse(
        _ body: Data,
        original: MobileTranslationRequest
    ) throws -> MobileTranslationResult {
        let text: String
        do {
            text = try responseText(from: body, engine: configuration.engine)
        } catch let error as MobileTranslationProviderError {
            throw error
        } catch {
            throw MobileTranslationProviderError.invalidResponse
        }
        let translations = parseNumberedTranslations(text)
        guard !translations.isEmpty else {
            throw MobileTranslationProviderError.invalidResponse
        }
        return MobileTranslationResult(
            segments: original.segments.map { segment in
                MobileTranslationSegment(
                    id: segment.id,
                    startTime: segment.startTime,
                    endTime: segment.endTime,
                    text: translations[segment.id] ?? segment.text
                )
            }
        )
    }

    private func responseText(from body: Data, engine: TranslationEngine) throws -> String {
        switch engine {
        case .openAICompatible:
            struct Choice: Decodable {
                struct Msg: Decodable { let content: String? }
                let message: Msg?
            }
            struct Reply: Decodable {
                let choices: [Choice]
            }
            let reply = try JSONDecoder().decode(Reply.self, from: body)
            let text = (reply.choices.first?.message?.content ?? "")
            guard !text.isEmpty else {
                throw MobileTranslationProviderError.invalidResponse
            }
            return text
        case .anthropicCompatible:
            struct Block: Decodable {
                let type: String
                let text: String?
            }
            struct Reply: Decodable {
                let content: [Block]
            }
            let reply = try JSONDecoder().decode(Reply.self, from: body)
            var parts: [String] = []
            for block in reply.content where block.type == "text" {
                if let text = block.text {
                    parts.append(text)
                }
            }
            let text = parts.joined()
            guard !text.isEmpty else {
                throw MobileTranslationProviderError.invalidResponse
            }
            return text
        case .appleTranslationLowLatency,
             .appleTranslationHighFidelity,
             .appleFoundationOnDevice,
             .appleFoundationPCC,
             .appleFoundationCloudPro:
            throw MobileTranslationProviderError.unsupportedEngine
        }
    }

    private func parseNumberedTranslations(_ text: String) -> [String: String] {
        var result: [String: String] = [:]
        for line in text.components(separatedBy: .newlines) {
            let trimmed = line.trimmingCharacters(in: .whitespacesAndNewlines)
            guard let separator = trimmed.firstIndex(of: "=") else {
                continue
            }
            let key = trimmed[..<separator].trimmingCharacters(in: .whitespacesAndNewlines)
            let value = trimmed[trimmed.index(after: separator)...].trimmingCharacters(in: .whitespacesAndNewlines)
            if !key.isEmpty, !value.isEmpty {
                result[key] = value
            }
        }
        return result
    }

    private func numberedInput(from segments: [MobileTranslationSegment]) -> String {
        segments.map { "\($0.id)=\($0.text)" }.joined(separator: "\n")
    }

    private func normalizedBearerSecret(_ secret: String) -> String {
        let trimmed = secret.trimmingCharacters(in: .whitespacesAndNewlines)
        return trimmed.lowercased().hasPrefix("bearer ")
            ? String(trimmed.dropFirst("bearer ".count)).trimmingCharacters(in: .whitespacesAndNewlines)
            : trimmed
    }

    private func endpointURL(baseURL: String, endpointPath: String) throws -> URL {
        var base = baseURL.trimmingCharacters(in: .whitespacesAndNewlines)
        while base.hasSuffix("/") { base.removeLast() }

        let path = endpointPath.hasPrefix("/") ? endpointPath : "/" + endpointPath
        let lowerBase = base.lowercased()
        let lowerPath = path.lowercased()
        let urlString: String
        if lowerBase.hasSuffix(lowerPath) {
            urlString = base
        } else if lowerBase.hasSuffix("/v1"), lowerPath.hasPrefix("/v1/") {
            urlString = base + String(path.dropFirst("/v1".count))
        } else {
            urlString = base + path
        }

        guard !base.isEmpty,
              let url = URL(string: urlString),
              let scheme = url.scheme?.lowercased(), scheme == "https",
              url.host != nil else {
            throw MobileTranslationProviderError.invalidConfiguration
        }
        return url
    }
}

