/// Platform-neutral progress. Unit counts may be bytes, files, segments, or logical steps.
public struct MobileTaskProgress: Codable, Sendable, Equatable {
    public var phase: MobileTaskPhase
    public var completedUnitCount: Int
    public var totalUnitCount: Int?

    public var fractionCompleted: Double? {
        guard let totalUnitCount, totalUnitCount > 0 else {
            return nil
        }

        let fraction = Double(completedUnitCount) / Double(totalUnitCount)
        return min(max(fraction, 0), 1)
    }

    public init(
        phase: MobileTaskPhase = .waiting,
        completedUnitCount: Int = 0,
        totalUnitCount: Int? = nil
    ) {
        self.phase = phase
        self.completedUnitCount = completedUnitCount
        self.totalUnitCount = totalUnitCount
    }
}

