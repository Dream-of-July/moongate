    case systemTimeLimit
    case systemDeferred
    case systemInterrupted
    case notResumable
    case requiresExternalPower
    case requiresUnmeteredNetwork
    case userVisibleNotificationRequired
}

public struct MobileBackgroundPolicy: Codable, Sendable, Equatable {
    public var execution: MobileBackgroundExecution
    public var resumability: MobileBackgroundResumability
    public var systemTimeLimitSeconds: Int?
    public var limits: [MobileBackgroundLimit]

    public var requiresForeground: Bool {
        execution == .foregroundRequired || limits.contains(.foregroundRequired)
    }

    public var isSystemLimited: Bool {
        switch execution {
        case .foregroundRequired:
            return limits.contains { limit in
                limit == .systemTimeLimit || limit == .systemDeferred || limit == .systemInterrupted
            }
        case .backgroundTransfer, .continuedProcessing, .scheduledWork, .systemManaged, .systemDeferred, .systemInterrupted:
            return true
        }
    }

    public var canResume: Bool {
        resumability == .resumable && !limits.contains(.notResumable)
    }

    public var allowsUnboundedBackgroundExecution: Bool {
        false
    }

    public init(
        execution: MobileBackgroundExecution = .foregroundRequired,
        resumability: MobileBackgroundResumability = .resumable,
        systemTimeLimitSeconds: Int? = nil,
        limits: [MobileBackgroundLimit] = []
    ) {
        self.execution = execution
        self.resumability = resumability
        self.systemTimeLimitSeconds = systemTimeLimitSeconds
        self.limits = limits
    }
}

