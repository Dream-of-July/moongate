    func testAllLanguageTablesHaveIdenticalKeySets() {
        let en = Set(LocalizationTables.en.keys)
        let hans = Set(LocalizationTables.zhHans.keys)
        let hant = Set(LocalizationTables.zhHant.keys)
        XCTAssertEqual(en, hans, "en 与 zh-Hans 键集不一致：\(en.symmetricDifference(hans))")
        XCTAssertEqual(en, hant, "en 与 zh-Hant 键集不一致：\(en.symmetricDifference(hant))")
        XCTAssertFalse(en.isEmpty)
    }

    func testNoTableValueIsEmpty() {
        for (lang, table) in [("en", LocalizationTables.en),
                              ("zh-Hans", LocalizationTables.zhHans),
                              ("zh-Hant", LocalizationTables.zhHant)] {
            for (key, value) in table {
                XCTAssertFalse(
                    value.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty,
                    "\(lang) 的 \(key) 文案为空"
                )
            }
        }
    }

