    func testAutoResolvesByPreferredLanguage() {
        XCTAssertEqual(AppLanguage.auto.resolved(preferredLanguages: ["en-US"]), .en)
        XCTAssertEqual(AppLanguage.auto.resolved(preferredLanguages: ["zh-Hans-CN"]), .zhHans)
        XCTAssertEqual(AppLanguage.auto.resolved(preferredLanguages: ["zh-Hant-TW"]), .zhHant)
        XCTAssertEqual(AppLanguage.auto.resolved(preferredLanguages: ["zh-TW"]), .zhHant)
        XCTAssertEqual(AppLanguage.auto.resolved(preferredLanguages: ["fr-FR"]), .en)
        // 显式选择优先于系统语言。
        XCTAssertEqual(AppLanguage.zhHant.resolved(preferredLanguages: ["en-US"]), .zhHant)
